#include <stdio.h>

int main()
{
	int num1,num2;
	
	printf("Enter Number 1:\n");
	scanf("%d",&num1);
	printf("Enter Number 2:\n");
	scanf("%d",&num2);
	printf("num1 + num2 = %d\n",num1+num2);
	printf("num1 - num2 = %d\n",num1-num2);
	printf("num1 & num2 = %d\n",num1&num2);
	printf("num1 | num2 = %d\n",num1|num2);
	printf("num1 ^ num2 = %d\n",num1^num2);
	
}