#include <stdio.h>

void main(void){
	// Basic datatypes
    printf("Size of char    is %d bytes\n", sizeof(char)); // 1 -> 8 bit
    printf("Size of int     is %d bytes\n", sizeof(int)); // 4 -> 32 bit
    printf("Size of float   is %d bytes\n", sizeof(float)); // 4 -> 32 bit
    printf("Size of double  is %d bytes\n", sizeof(double)); // 8 -> 64 bit
} 

	